class AppCard(QFrame):
    def __init__(self, name, dev, app_id, icon_filename):
        super().__init__()
        self.name_text = name
        self.setFixedSize(210, 280)
        self.setStyleSheet("QFrame { background-color: white; border: 1px solid #dcdcdc; border-radius: 12px; }")

        layout = QVBoxLayout(self)
        layout.setContentsMargins(15, 15, 15, 15)

        # 1. Icon
        self.icon_label = QLabel()
        self.icon_label.setFixedSize(70, 70)
        self.icon_label.setAlignment(Qt.AlignmentFlag.AlignCenter)
        self.icon_label.setStyleSheet("border: none; background: transparent;")
        
        base_path = os.path.dirname(os.path.abspath(__file__))
        icon_path = os.path.join(base_path, "icons", icon_filename)

        if os.path.exists(icon_path):
            pix = QPixmap(icon_path)
            self.icon_label.setPixmap(pix.scaled(70, 70, Qt.AspectRatioMode.KeepAspectRatio, Qt.TransformationMode.SmoothTransformation))
        else:
            self.set_placeholder(name)
        layout.addWidget(self.icon_label, alignment=Qt.AlignmentFlag.AlignCenter)

        # 2. Name
        name_lbl = QLabel(name)
        name_lbl.setAlignment(Qt.AlignmentFlag.AlignCenter)
        name_lbl.setStyleSheet("font-weight: bold; font-size: 13px; color: black; border: none; background: transparent;")
        name_lbl.setWordWrap(True)
        layout.addWidget(name_lbl)

        # 3. Creator
        dev_lbl = QLabel(dev)
        dev_lbl.setAlignment(Qt.AlignmentFlag.AlignCenter)
        dev_lbl.setStyleSheet("color: #666; font-size: 10px; border: none; background: transparent;")
        layout.addWidget(dev_lbl)

        layout.addStretch()

        # 4. Button
        self.btn = QPushButton("Install")
        self.btn.setStyle(QStyleFactory.create("Fusion")) 
        self.btn.setCursor(Qt.CursorShape.PointingHandCursor)
        self.btn.setFixedSize(180, 40)
        self.btn.setStyleSheet("""
            QPushButton {
                background-color: #005fb8;
                color: white;
                border-radius: 8px;
                font-weight: bold;
                border: none;
            }
            QPushButton:hover { background-color: #0078d4; }
            QPushButton:pressed { background-color: #004275; }
        """)
        self.btn.clicked.connect(lambda: self.install(app_id))
        layout.addWidget(self.btn, alignment=Qt.AlignmentFlag.AlignCenter)

    def set_placeholder(self, name):
        pix = QPixmap(70, 70)
        pix.fill(QColor("#0078d4"))
        p = QPainter(pix)
        p.setPen(QColor("white"))
        p.setFont(QFont("Segoe UI", 20, QFont.Weight.Bold))
        p.drawText(pix.rect(), Qt.AlignmentFlag.AlignCenter, name.upper()[:1])
        p.end()
        self.icon_label.setPixmap(pix)

    def install(self, app_id):
        # 1. Change Visual
        self.btn.setEnabled(False)
        self.btn.setText("Installing...")
        self.btn.setStyleSheet("background-color: #cccccc; color: #666; border-radius: 8px; border: none;")
        
        # Getting the path to the folder where code is located
        base_path = os.path.dirname(os.path.abspath(__file__))

        try:
            # Check it app_id or .yaml
            if str(app_id).lower().endswith(('.yaml', '.yml')):
                # Get a path to .yaml
                manifest_path = os.path.join(base_path, str(app_id))
                
                # Command for installing With manifest
                # We wrap the full path in quotes to handle spaces correctly
                cmd = f'winget install --manifest "{manifest_path}" --silent --override "/VERYSILENT /SP- /NORESTART" --accept-package-agreements --accept-source-agreements'
                
                # Opening with shell=True
                subprocess.Popen(cmd, shell=True)
                print(f"DEBUG: WinGet started installing manifest: {manifest_path}")
                
            else:
                # Regular installation using the repository ID.
                cmd = f'winget install --id {app_id} --silent --accept-package-agreements --accept-source-agreements'
                subprocess.Popen(cmd, shell=True)
                print(f"DEBUG: WinGet started installing ID: {app_id}")

        except Exception as e:
            print(f"Critical Error: {e}")
            self.btn.setEnabled(True)
            self.btn.setText("Error")

